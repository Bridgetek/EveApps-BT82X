{
    Gesture_Touch_t *ges = utils_gestureGet();
    for (int i = 0; i < NUM_HEXAGON_VARIANTS; i++)
    {
        if (ges->tagPressed == tag_variants[i])
        {
            color_pressed = color_picker_table[selected_color_nth]->hexagon_variant[i];
        }
    }
    return PAGE_CONTINUE;
}
