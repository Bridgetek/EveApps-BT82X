static void page_sketch_init_one()
{
    static bool is_page_sketch_init = 0;
    if (is_page_sketch_init == 1)
        return;
    is_page_sketch_init = 1;

    // Sketch setup
    sketch1.w = 1920;
    sketch1.h = 1080;
    sketch1.bitmap_handler = 6; // utils_BitmapHandler_get();
    sketch1.bits_per_pixel = min(8, utils_searchBitsPerPixel(sketch1.bitmap_fmt));
    sketch1.linestride = sketch1.w * sketch1.bits_per_pixel / 8;
    sketch1.memory_size = sketch1.linestride * sketch1.h;

    memcpy(&sketch2, &sketch1, sizeof(sketch1));
    memcpy(&sketch3, &sketch1, sizeof(sketch1));

    memcpy(sketch1.name, "Pen 1", sizeof("Sketch1"));
    memcpy(sketch2.name, "Pen 2", sizeof("Sketch2"));
    memcpy(sketch3.name, "Pen 3", sizeof("Sketch3"));

    sketch1.tagval = utils_tagAlloc(TAG_INC);
    sketch2.tagval = utils_tagAlloc(TAG_INC);
    sketch3.tagval = utils_tagAlloc(TAG_INC);

    sketch1.eve_ramg_ptr = utils_ddrAllocAlignment(sketch1.memory_size, 32);
    sketch2.eve_ramg_ptr = utils_ddrAllocAlignment(sketch2.memory_size, 32);
    sketch3.eve_ramg_ptr = utils_ddrAllocAlignment(sketch3.memory_size, 32);

    cmd_memzero(PHOST, sketch1.eve_ramg_ptr, sketch1.memory_size);
    cmd_memzero(PHOST, sketch2.eve_ramg_ptr, sketch2.memory_size);
    cmd_memzero(PHOST, sketch3.eve_ramg_ptr, sketch3.memory_size);

    IMG_ICON_LINE_WIDTH2->tagval = IMG_ICON_LINE_WIDTH1->tagval;
    IMG_ICON_LINE_WIDTH3->tagval = IMG_ICON_LINE_WIDTH1->tagval;
    IMG_ICON_LINE_WIDTH4->tagval = IMG_ICON_LINE_WIDTH1->tagval;

    color_picker->scale = color_picker_scale;
    hexagon_boxw = hexagon_boxw * color_picker_scale / 100;
    hexagon_boxh = hexagon_boxh * color_picker_scale / 100;

    panel_color.x = PHOST->Width / 2 - panel_color.w / 2;
    panel_color.y = 20; // PHOST->Height / 2 - panel_color.h / 2;

    panel_color_picker.w = color_picker->w * color_picker_scale / 100;
    panel_color_picker.h = color_picker->h * color_picker_scale / 100;
    panel_color_picker.x = panel_color.x + panel_color.w / 2 - panel_color_picker.w / 2;
    panel_color_picker.y = panel_color.y + panel_color.h / 2 - panel_color_picker.h / 2;

    panel_color_preview.x = panel_color.x + 40;
    panel_color_preview.y = panel_color.y + panel_color.h / 2 - panel_color_preview.h / 2;

    for (int i = 0; i < NUM_HEXAGON_VARIANTS; i++)
    {
        tag_variants[i] = utils_tagAlloc(TAG_INC);
    }

    tag_btn_select = utils_tagAlloc(TAG_INC);
    tag_btn_close = utils_tagAlloc(TAG_INC);
    tag_btn_color = utils_tagAlloc(TAG_INC);
    tag_btn_clear = utils_tagAlloc(TAG_INC);
    tag_btn_clearall = utils_tagAlloc(TAG_INC);

    selected_color_nth = 84;
    rownth = 6;
    colnth = 6;
    colnth_indent = 0;
    color_pressed = color_picker_table[selected_color_nth]->hexagon_code;

    sketch1.color32 = color_pressed;
    sketch2.color32 = color_pressed;
    sketch3.color32 = color_pressed;
}

